CREATE TABLE PRODUCTOS (
ID_PRODUCTO NUMBER(10) CONSTRAINT PK_PRODUCTO PRIMARY KEY,
NOMBRE_PRODUCTO VARCHAR2(25),
PRECIO_UNITARIO NUMBER(25),
