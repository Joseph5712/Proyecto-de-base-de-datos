CREATE TABLE PROVEEDOR(
ID_PROVEEDOR NUMBER(10) CONSTRAINT PK_PERSONA  PRIMARY KEY,
IDENTIFICACION NUMBER(20),
NOMBRE VARCHAR2(25),
EMAIL VARCHAR2(25),
DIRECCION VARCHAR2(50),
CONTACTO VARCHAR2(50),
ID_TIPO_PROV NUMBER(10), 
FECHA_INGRESO DATE,
ESTADO VARCHAR2(10)--ACTIVO O NO
);
 ALTER TABLE PROVEEDOR ADD CONSTRAINT UK_IDENTIFICACION UNIQUE(IDENTIFICACION);
ALTER TABLE PROVEEDOR ADD CONSTRAINT CK_ESTADO CHECK (ESTADO IN ('ACTIVO','NO ACTIVO'));


CREATE TABLE TIPO_PROVEEDOR(
ID_TIPO_PRO NUMBER(10) CONSTRAINT PK_ID_TIPO_PRO  PRIMARY KEY,
TIPO_PRO VARCHAR2(20)
);


CREATE TABLE CLIENTE (
ID_CLIENTE NUMBER(10) CONSTRAINT PK_ID_CLIENTE PRIMARY KEY,
IDENTIFICACION NUMBER(20),
NOMBRE_ VARCHAR2(25),
APELLIDO1 VARCHAR2(50),
APELLIDO2  VARCHAR2(50),
SEXO CHAR,
FECHA_NACIMIENTO DATE,
FECHA_INGRESO DATE,
ESTATUS VARCHAR2(30),
CATEGORIA VARCHAR2(10),
LIMITE_CREDITO NUMBER(10),
ID_DIRECCION NUMBER(10) 
);
ALTER TABLE CLIENTE ADD CONSTRAINT CK_SEXO CHECK (SEXO IN ('M','F'));
 ALTER TABLE CLIENTE ADD CONSTRAINT UK_CLIEN_IDENTIFICACION UNIQUE(IDENTIFICACION);

CREATE TABLE CONTACTO(
ID_CONTACTO NUMBER(10)CONSTRAINT PK_ID_CONTACTO  PRIMARY KEY,
IDCLIENTE NUMBER(10),
TIPO VARCHAR2(10),
VALOR NUMBER(20),
ESTADO VARCHAR2(20)
);


CREATE TABLE EMPLEADO (
ID_EMPLEADO NUMBER(10) CONSTRAINT PK_ID_EMPLEADO PRIMARY KEY,
NOMBRE VARCHAR2(25),
NOMBRE2 VARCHAR2(25),
APELLIDO1 VARCHAR2(50),
APELLIDO2  VARCHAR2(50),
EMAIL VARCHAR2(25),
ID_JEFE NUMBER(25),
ID_DEPTO NUMBER(20),
ID_PUESTO NUMBER(20),
SEXO CHAR
);
ALTER TABLE EMPLEADO ADD CONSTRAINT CK_EMPLEADO_SEXO CHECK (SEXO IN ('M','F'));

CREATE TABLE PUESTO(
